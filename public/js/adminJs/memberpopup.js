
function openPopup() {
    // เรียกใช้งานและแสดง popup ที่คุณต้องการ
    // โดยอาจใช้ CSS หรือ JavaScript เพื่อแสดง popup
    // ตัวอย่างเช่น:
    const popup = document.getElementById('myPopup');
    popup.style.display = 'block';
}


// สร้างฟังก์ชันสำหรับปิด Popup
function closePopup() {
    const popup = document.getElementById('myPopup');
    popup.style.display = 'none';
}











